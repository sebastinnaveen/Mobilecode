package com.vz.hackathon;

public class CustomerInfo {
	String msgTech, customerName, customerLoc, customerNum;

	public String getMsgTech() {
		return msgTech;
	}

	public void setMsgTech(String msgTech) {
		this.msgTech = msgTech;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getCustomerLoc() {
		return customerLoc;
	}

	public void setCustomerLoc(String customerLoc) {
		this.customerLoc = customerLoc;
	}

	public String getCustomerNum() {
		return customerNum;
	}

	public void setCustomerNum(String customerNum) {
		this.customerNum = customerNum;
	}

}
